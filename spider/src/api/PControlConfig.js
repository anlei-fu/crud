import { HttpClient } from "./HttpClient"

class PControlConfig extends HttpClient {
    constructor() {
        super('PControlConfig', '/p-control-config')
    }

            /**
         * @description
         * method:put 
         * 
         * @param {Object} params
         * @return {Promise<Object>}
         */
        update(params) {
                return this.put('', params);
        }

        /**
         * @description
         * method:get 
         * 
         * @param {Object} params
         * @return {Promise<Object>}
         */
        getById(params) {
                return this.get('{id}', params);
        }


}

const pControlConfig = new PControlConfig();
export {
    pControlConfig
}