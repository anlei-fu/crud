import { HttpClient } from "./HttpClient"

class PColorConfig extends HttpClient {
    constructor() {
        super('PColorConfig', '/p-color-config')
    }

            /**
         * @description
         * method:put 
         * 
         * @param {Object} params
         * @return {Promise<Object>}
         */
        update(params) {
                return this.put('', params);
        }

        /**
         * @description
         * method:get 
         * 
         * @param {Object} params
         * @return {Promise<Object>}
         */
        getById(params) {
                return this.get('{id}', params);
        }


}

const pColorConfig = new PColorConfig();
export {
    pColorConfig
}